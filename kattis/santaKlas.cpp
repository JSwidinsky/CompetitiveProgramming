typedef long double ld;
ld PI = acos(-1);

int main() {
  ios::sync_with_stdio(0); cin.tie(0);
  int d, a; cin >> d >> a;
  
  if(a >= 0 && a <= 180) cout << "safe" << endl;
  else {
    
    ld ans = d / sin(((a-180)*PI)/180.f);

    cout << (int)floor(ans) << endl;
  }
}

